import bcrypt from "bcryptjs";
import mongoose from "mongoose";

var Schema = mongoose.Schema;
let UserModelSchema = new Schema({
  email: {
    type: String,
    required: true,
    unique: true,
  },
  username: {
    type: String,
    required: true,
    unique: true,
  },
  passwordHash: {
    type: String,
    required: true,
  },
  createdAt: {
    type: Date,
    immutable: true,
    default: () => Date.now()
  },
  updatedAt: {
    type: Date,
    default: () => Date.now()
  }
});

export default mongoose.model("UserModel", UserModelSchema);
